public class DtoSpeaker {

    private String nameSpeakerDto;
    private String descriptionDto;
    private String chargeDto;
    private String countryDto;

    public String getNameSpeakerDto() {
        return nameSpeakerDto;
    }

    public void setNameSpeakerDto(String nameSpeakerDto) {
        this.nameSpeakerDto = nameSpeakerDto;
    }

    public String getDescriptionDto() {
        return descriptionDto;
    }

    public void setDescriptionDto(String descriptionDto) {
        this.descriptionDto = descriptionDto;
    }

    public String getChargeDto() {
        return chargeDto;
    }

    public void setChargeDto(String chargeDto) {
        this.chargeDto = chargeDto;
    }

    public String getCountryDto() {
        return countryDto;
    }

    public void setCountryDto(String countryDto) {
        this.countryDto = countryDto;
    }
}
